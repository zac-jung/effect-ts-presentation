// =============================================================================
// 02 BAD. Promise는 호출하는 순간 실행된다
// =============================================================================
// async function은 값처럼 보이지만, 호출하면 즉시 Promise가 만들어지고
// 함수 본문 실행이 시작됩니다. 즉, side effect 시작 시점을 호출자가 완전히
// 값으로 다루기 어렵습니다.

// =============================================================================
// 1. async 함수: 호출 자체가 실행 시작점이다
// =============================================================================

const fetchUserBad = async () => {
  console.log("[bad] 호출 즉시 네트워크 요청이 시작됐다고 생각해보자")
  return { id: "1", name: "Ada" }
}

// =============================================================================
// 2. 이미 시작된 Promise를 나중에 await할 뿐이다
// =============================================================================

export const demoBad = async () => {
  const promise = fetchUserBad()
  console.log("[bad] 이 로그보다 fetchUserBad의 side effect가 먼저 실행됨")
  console.log(await promise)
}
