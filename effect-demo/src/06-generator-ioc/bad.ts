// =============================================================================
// 06 BAD. async/await는 Promise를 기다릴 뿐이다
// =============================================================================
// 이 파일은 나쁜 코드라기보다 비교 기준입니다.
// 일반 async/await는 읽기 쉽지만, 언어 문법 자체가 dependency lookup,
// retry/timeout, interruption, error channel을 제공하지는 않습니다.

// =============================================================================
// 1. 구체 dependency가 코드 안에 직접 들어와 있다
// =============================================================================

const http = {
  getUser: async () => ({ id: "1", name: "Ada" })
}

// =============================================================================
// 2. async function: 제어권은 함수 내부에 있다
// =============================================================================
// `await`는 Promise를 기다리는 문법입니다.
// runtime이 service를 주입하거나, 실패 channel을 타입으로 추적하거나,
// cleanup/interruption을 통합 관리하는 모델은 아닙니다.

export async function loadUserAsyncAwait() {
  const user = await http.getUser()
  console.log(`loaded ${user.name}`)
  return user
}
